class BlocksSearcher
{
public:
	static void DFS();
	static std::uint64_t generated;
	static std::uint64_t found;
};

static constexpr size_t STACK_SIZE = 2200;

void BlocksSearcher::DFS() {
	__m256i avxOrStack[STACK_SIZE / 4];
	__m256i avxXorStack[STACK_SIZE / 4];
	std::uint64_t* orStack = (std::uint64_t*) avxOrStack;
	std::uint64_t* xorStack = (std::uint64_t*) avxXorStack;
	std::array<unsigned, 68> esp;
	unsigned sp = 1;
	std::copy(placements + placementIndices[0][0], placements + placementIndices[0][0] + placementIndices[0][1], orStack);
	std::copy(placements + placementIndices[0][0], placements + placementIndices[0][0] + placementIndices[0][1], xorStack);
	orStack[70] = ~0LLU;
	xorStack[70] = 0LLU;
	orStack[71] = ~0LLU;
	xorStack[71] = 0LLU;
	sp = 70;

	while (true) {
		if(--sp > STACK_SIZE) {
			break;
		}
		if (orStack[sp] != xorStack[sp]) {
			continue;
		}
		//int piece = pieceCache[__builtin_popcountll(orStack[sp])];
		unsigned onBits = __builtin_popcountll(orStack[sp]);
		esp[onBits] = sp;
		const unsigned placementsStart = placementsStarts[onBits];
		const unsigned placementsCount = placementsCounts[onBits];
		std::uint64_t val = orStack[sp];
		if (onBits < 59) {
			assert(onBits < 59);
			orStack[sp] = ~0ULL;
			sp = (sp + 3) & ~3U;
			__m256i wideVal = _mm256_set1_epi64x(val);
			for (unsigned i = 0; i < placementsCount; i += 4) {
				*(__m256i *)(orStack + sp + i) = _mm256_or_si256(*(__m256i *) (placements + placementsStart + i), wideVal);
				*(__m256i *)(xorStack + sp + i) = _mm256_xor_si256(*(__m256i *) (placements + placementsStart + i), wideVal);
			}
			generated += placementsCount;
			sp += placementsCount;
		} else {
			for (unsigned i = 0; i < placementsCount; ++i) {
				if ((placements[placementsStart + i] | val) == ~0LLU) {
					++found;
					break;
				}
			}
		}

		if (generated % 1000000000L < (generated - placementsCount) % 1000000000L) {
			printf("generated : %llu found: %llu\n", generated, found);
		}
	}
}


std::uint64_t BlocksSearcher::generated = 0;

std::uint64_t BlocksSearcher::found = 0;

int main() {
	unsigned long before = std::chrono::duration_cast<std::chrono::milliseconds>(std::chrono::system_clock::now().time_since_epoch()).count();
	BlocksSearcher::DFS();
	unsigned long after = std::chrono::duration_cast<std::chrono::milliseconds>(std::chrono::system_clock::now().time_since_epoch()).count();
	printf("time: %lu\n", (unsigned long) (after - before));
	return 0;
}
